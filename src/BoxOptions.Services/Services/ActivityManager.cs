    public class ActivityManager : IActivityManager, IStartable
    {
        private readonly IActivityRepository _activityRepository;

        public ActivityManager(IActivityRepository activityRepository)
        {
            _activityRepository = activityRepository;
        }
        public async Task<IEnumerable<IActivity>> GetActivities(string instrument)
        {
            return await _activityRepository.GetActivitiesByInstrument(instrument);
        }

        public async Task<IActivity> GetActivityByName(string instrument, string name)
        {
            return await _activityRepository.GetActivitiesByInstrumentAndName(instrument, name);
        }

        public async Task<IActivity> GetDefaultActivity(string instrument)
        {
            var res = await _activityRepository.GetActivitiesByInstrument(instrument);
            return res.First(x => x.IsDefault);
        }


        public async Task SetActivity(IActivity activity)
        {
            await _activityRepository.InsertOrReplaceActivities(new IActivity[] { activity });
            if (activity.IsDefault)
                await SetDefaultActivity(activity);
        }

        public async Task SetDefaultActivity(IActivity activity)
        {
            var activities = await _activityRepository.GetActivitiesByInstrument(activity.Instrument);
            var updated = activities.Select(x => new Activity
            {
                Id = x.Id,
                 Instrument = x.Instrument,
                 IsDefault = x.Id == activity.Id 
                    ? true 
                    : false,
                 ActivityArray = x.ActivityArray,
                 Name = x.Name
            });
            await _activityRepository.InsertOrReplaceActivities(updated);
        }

        public void Start()
        {
            // Check Default Activities: EURCHF, BTCUSD
            var activitiesEURCHF = Task.Run(async () => await GetActivities("EURCHF")).Result;
            if (activitiesEURCHF.Count() == 0)
            {
                var newEURCH = new Activity
                {
                    Id = Guid.NewGuid().ToString(),
                    ActivityArray = GetEURCFHDefault(),
                    Instrument = "EURCHF",
                    IsDefault = true,
                    Name = "newEURCHF10min_0_00018_weekendYES"
                };
                Task.Run(async () => await _activityRepository.InsertOrReplaceActivities(new IActivity[] { newEURCH })).Wait();
            }

            var activitiesBTCUSD = Task.Run(async () => await GetActivities("BTCUSD")).Result; 
            if (activitiesBTCUSD.Count() == 0)
            {
                var newBTCUSD = new Activity
                {
                    Id = Guid.NewGuid().ToString(),
                    ActivityArray = GetBTCUSDDefault(),
                    Instrument = "BTCUSD",
                    IsDefault = true,
                    Name = "newBTCUSD10min_0_003_weekendNO"
                };
                Task.Run(async () => await _activityRepository.InsertOrReplaceActivities(new IActivity[] { newBTCUSD })).Wait();
            }
        }

